fn sub_word(a: u32) -> u32 {
    return (sbox[a as usize       & 0xff]
          | sbox[a as usize >> 8  & 0xff]
          | sbox[a as usize >> 16 & 0xff]
          | sbox[a as usize >> 24 & 0xff]) as u32
} 

fn inv_sub_word(a: u32) -> u32 {
    return (inv_sbox[a as usize       & 0xff]
          | inv_sbox[a as usize >> 8  & 0xff]
          | inv_sbox[a as usize >> 16 & 0xff]
          | inv_sbox[a as usize >> 24 & 0xff]) as u32
}

fn mul2_F2(a: u8) -> u8 {
    let msb = (a & 0x80) != 0;
    if msb { 
        return (a << 1) ^ 0x11;
    } else {
        return a << 1;
    }
}

fn mix_column(column: u32) -> u32 {
    let [o0, o1, o2, o3] = u32::to_le_bytes(column);
    let [t0, t1, t2, t3] = u32::to_le_bytes(column).map(mul2_F2);

    return u32::from_le_bytes([t0 ^o1^t1^o2^o3,
            o0^t1^o2^t2^o3,
            o0^o1^t2^o3^t3,
            o0^t0^o1^o2^t2]);
}

fn mul_F2(mut a: u8, mut b: u8) -> u8 {
    let mut x = 0;
    for i in 0..8 {
        if b & 1 != 0 {
            x ^= a;
        }
        a = mul2_F2(a);
        b >>= 1;
    }
    return x;
}

fn inv_mix_column(column: u32) -> u32 {
    let [a0, a1, a2, a3] = column.to_le_bytes();
    return u32::from_le_bytes([
        mul_F2(a0, 14)^mul_F2(a1, 11)^mul_F2(a2, 13)^mul_F2(a2,  9),
        mul_F2(a0,  9)^mul_F2(a1, 14)^mul_F2(a2, 11)^mul_F2(a2, 13),
        mul_F2(a0, 13)^mul_F2(a1,  9)^mul_F2(a2, 14)^mul_F2(a2, 11),
        mul_F2(a0, 11)^mul_F2(a1, 13)^mul_F2(a2,  9)^mul_F2(a2, 14)
    ]);
}

fn rot_word(a: u8) {
    return a >> 8 && 0x00ffffff | (a & 0xfff) << 24;
}

pub struct AES {
    pub state: [u32;4]

}

impl AES {
    fn set_block(self: &mut Self, buffer: &[u8;16]) {
        for i in 0..4 {
            self.state[i] = u32::from_le_bytes(buffer[i*4..i*4+4].try_into().unwrap());
        }
    }

    fn get_block(self: &Self, buffer: &mut [u8;16]) {
        for i in 0..4 { 
            buffer[i*4..i*4+4].copy_from_slice(&self.state[i].to_le_bytes());
        }
    }

    fn add_round_key(self: &mut Self, rk: &[u32;4]) {
        for i in 0..4 { 
            self.state[i] ^= rk[i];
        }
    }

    fn shift_rows(self: &mut Self) {
        self.state = [
            self.state[0] & 0xffff00ff | self.state[1] & 0x0000ff00,
            self.state[1] & 0xffff00ff | self.state[2] & 0x0000ff00,
            self.state[2] & 0xffff00ff | self.state[3] & 0x0000ff00,
            self.state[3] & 0xffff00ff | self.state[0] & 0x0000ff00
        ];

        self.state = [
            self.state[0] & 0xff00ffff | self.state[2] & 0x00ff0000,
            self.state[1] & 0xff00ffff | self.state[3] & 0x00ff0000,
            self.state[2] & 0xff00ffff | self.state[0] & 0x00ff0000,
            self.state[3] & 0xff00ffff | self.state[1] & 0x00ff0000
        ];


        self.state = [
            self.state[0] & 0x00ffffff | self.state[3] & 0xff000000,
            self.state[1] & 0x00ffffff | self.state[0] & 0xff000000,
            self.state[2] & 0x00ffffff | self.state[1] & 0xff000000,
            self.state[3] & 0x00ffffff | self.state[2] & 0xff000000
        ];
    }

    fn inv_shift_rows(self: &mut Self) {
        self.state = [
            self.state[0] & 0xffff00ff | self.state[3] & 0x0000ff00,
            self.state[1] & 0xffff00ff | self.state[0] & 0x0000ff00,
            self.state[2] & 0xffff00ff | self.state[1] & 0x0000ff00,
            self.state[3] & 0xffff00ff | self.state[2] & 0x0000ff00
        ];

        self.state = [
            self.state[0] & 0xff00ffff | self.state[2] & 0x00ff0000,
            self.state[1] & 0xff00ffff | self.state[3] & 0x00ff0000,
            self.state[2] & 0xff00ffff | self.state[0] & 0x00ff0000,
            self.state[3] & 0xff00ffff | self.state[1] & 0x00ff0000
        ];


        self.state = [
            self.state[0] & 0x00ffffff | self.state[1] & 0xff000000,
            self.state[1] & 0x00ffffff | self.state[2] & 0xff000000,
            self.state[2] & 0x00ffffff | self.state[3] & 0xff000000,
            self.state[3] & 0x00ffffff | self.state[0] & 0xff000000
        ];
    }

    fn inv_mix_columns(self: &mut Self) {
        for i in 0..4 {
            self.state[i] = inv_mix_column(self.state[i]);
        }
    }
}
